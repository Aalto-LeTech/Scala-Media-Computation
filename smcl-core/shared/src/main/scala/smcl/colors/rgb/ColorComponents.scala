package smcl.colors.rgb


/**
 * Constants for RGBA color components.
 *
 * @author Aleksi Lukkarinen
 */
object ColorComponents {




  /**
   * Base class for color component constants.
   */
  sealed abstract class ColorComponent




  /**
   * A constant representing the red color component.
   */
  case object Red extends ColorComponent




  /**
   * A constant representing the green color component.
   */
  case object Green extends ColorComponent




  /**
   * A constant representing the blue color component.
   */
  case object Blue extends ColorComponent




  /**
   * A constant representing the opacity (alpha) color component.
   */
  case object Opacity extends ColorComponent




}
